#include <iostream>
using namespace std;

int main() {
    int son;
    cout << "Sonni kiriting: ";
    cin >> son;

    // 1.
    cout << "10 ga oshirilgan son: " << son + 10 << endl;

    // 2.
    cout << "10 ga kamaytirilgan son: " << son - 10 << endl;

    // 3.
    cout << "10 marta oshirilgan son: " << son * 10 << endl;

    // 4. 
    cout << "Sonning kvadrati: " << son * son << endl;

    return 0;
}
